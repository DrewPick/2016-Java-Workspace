/**InClass3.java
 * This program shows the user how formating could be applied
 * @author Drew Pickett
 * 10/8/16
 */

public class InClass3 {

    public static void main(String[] args) {
        
        // Prints "Knowledge is Power"
        System.out.println("Knowledge is Power");
        
        System.out.println("");
        
        // Prints a wraped text of "Knowledge is Power"
        System.out.println("Knowledge");
        System.out.println("   is   ");
        System.out.println("  Power  ");
        
        System.out.println("");
        
        // Prints out the wraped version of "Knowledge is Power" but in a box
        System.out.println("=============");
        System.out.println("| Knowledge |");
        System.out.println("|    is     |");
        System.out.println("|   Power   |");
        System.out.println("=============");
    }
    
}
